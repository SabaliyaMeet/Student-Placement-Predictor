from sklearn.metrics import confusion_matrix

def get_confusion_details(y_true, y_pred):
    tn, fp, fn, tp = confusion_matrix(y_true, y_pred).ravel()
    return {
        'TP': tp,
        'FP': fp,
        'FN': fn,
        'TN': tn
    }

# Get predictions
